            <p>Las políticas institucionales de capacidad académica del Plan Institucional de Desarrollo, 2018-2023, aluden a consolidar la habilitación y capacitación del personal docente, así como el fortalecimiento de los cuerpos académicos.</p>
      
            <p>En función de ello; la Universidad Autónoma del Estado de Morelos, parte de considerar la articulación de programas de mejoramiento al trabajo docente, desde el abordaje de la evaluación del desempeño del personal académico y de su formación y actualización permanente como elementos que constituyen en sí mismos, los cimientos hacia el desarrollo profesional de la docencia.</p>
            
            <p>Respecto a los instrumentos de evaluación docente: encuesta de opinión sobre el desempeño docente dirigido a los estudiantes, y una autoevaluación docente, fueron elaboradas a partir del planteamiento de 6 dimensiones: actividad de aprendizaje, ubicación y organización de actividades, manejo grupal, autonomía del alumno, evaluación de los aprendizajes, ética y valores.</p>
